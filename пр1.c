#include <stdio.h>


int main()
{
  int t1, t2, t3; /* об'являю змінні */
  float t4;
  printf("Ведіть три значення:\n" ); 
  scanf("%d",&t1); /* Ввожу змінні */
  scanf("%d",&t2);
  scanf("%d",&t3);
  
  t4= (t1*t2*t3)/(t2*t1+t3*t2+t1*t3); /* Робоча формула */
  printf("Час, необхідний для з'їдання пирога:" );
  printf("%2.2f", t4); /* Виведення змінної */
  

  return 0;
}